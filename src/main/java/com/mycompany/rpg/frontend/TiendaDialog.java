    private void btnComprarPrjActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarPrjActionPerformed
        if (rBtnCaballero.isSelected()) {
            System.out.println("caballero");
            tienda.comprarPersonajeJugable(1);
        } else if (rBtnArquero.isSelected()) {
            System.out.println("arquero");
            tienda.comprarPersonajeJugable(2);
        } else if (rBtnMago.isSelected()) {
            System.out.println("mago");
            tienda.comprarPersonajeJugable(3);
        } else if (rBtnGigante.isSelected()) {
            System.out.println("gigante");
            tienda.comprarPersonajeJugable(4);
        } else if (rBtnDragon.isSelected()) {
            System.out.println("dragon");
            tienda.comprarPersonajeJugable(5);
        } else {
            alertMessage("Debe seleccionar un personaje a comprar");
        }
        refrecshLabelOro();
    }//GEN-LAST:event_btnComprarPrjActionPerformed

